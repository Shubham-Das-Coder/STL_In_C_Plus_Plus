#include <bits/stdc++.h>

using namespace std;

struct node
{
    string str;
    int i;
    double d;
    char ch;
};

int main()
{
    node shubham;
    shubham.str = "Shubham";
    shubham.i = 19;
    shubham.d = 97.5;
    shubham.ch = 'S';
    cout << shubham.str << endl;
    cout << shubham.i << endl;
    cout << shubham.d << endl;
    cout << shubham.ch << endl;
}